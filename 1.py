'''
Дано целое число N (>0) и строка S. Преобразовать строку S в строку длины N 
следующим образом: если длина строки S больше N, то отбросить первые символы, 
если длина строки S меньше N, то в ее начало добавить символы «.» (точка).
'''


def transform_string(N, S):
    if len(S) > N:
        # Если длина строки S больше N, отбрасываем первые символы
        result = S[-N:]
    elif len(S) < N:
        # Если длина строки S меньше N, добавляем символы '.' в начало
        result = '.' * (N - len(S)) + S
    else:
        result = S
    
    return result

# Пример использования
N = int(input("Введите целое число N: "))
S = input("Введите строку S: ")

transformed_string = transform_string(N, S)
print("Результат преобразования:", transformed_string)
